# Work-around to make sure students correctly name their function. Essentially,
# I just copied the contents of include_from_LOAD_PATH into the new temp module
# because using include_from_LOAD_PATH would load the function into a scope that
# was not accessible after the end of the module.
function _getEmbeddedFunction(fcnStr; moduleName="tmp")
    cmds = "module $moduleName; " *
           "found = false; abs_path = \"\"; " *
           "for path in LOAD_PATH; global abs_path = joinpath(path, \"$fcnStr.jl\"); " *
           "if isfile(abs_path); global found = true; break; end; end; " *
           "if !found; msg = \"Couldn't find file $fcnStr.jl on LOAD_PATH\"; " *
           "pad = \"*\" ^ 3; bar = \"*\" ^ (length(msg) + 2); " *
           "throw(ErrorException(\"\\n\\n\$pad\$bar\$pad\\n\$pad \$msg \$pad\\n\$pad\$bar\$pad\\n\")); " *
           "end; " *
           "include(abs_path); " *
           "end; " *
           "$moduleName.$fcnStr"
    return eval(Meta.parse(cmds))
end

