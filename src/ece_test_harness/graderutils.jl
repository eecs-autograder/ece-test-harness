# File postfixes
GRADER_POSTFIX    = "_grader"
SOLUTION_POSTFIX  = "_sol"

# String denoting beginning of test results
# This mutch match the TEST_RESULT_REGEX defined in ../src/main.py
TEST_RESULT_REGEX = "***** RESULTS *****"

# Test status strings
PASS              = "pass"
FAIL              = "fail"
ERROR             = "error"

#-------------------------------------------------------------------------------
# Private constants
#-------------------------------------------------------------------------------
# Default test case arguments
_DEFAULT_TOL      = 0
_DEFAULT_CHECKS   = []

#-------------------------------------------------------------------------------
# Public methods
#-------------------------------------------------------------------------------
#
# Generate a test check
#
# Args:
#   fcn: A function returning true/false
#   arg: The output argument number to which to apply fcn()
#   msg: The error message to display when fcn returns false
#
function generateCheck(fcn, arg, msg)
    return Dict(
        "fcn" => fcn,
        "arg" => arg,
        "msg" => msg,
    )
end

#
# Generate a single test case
#
# Args:
#   args: Function input. If > 1 inputs are required, pass them in as a tuple
#   kwargs: Optional tuple of keyword arguments, where each keyword argument is
#     itself specified as a tuple of the form (Symbol, val). For example, to
#     set a keyword argument "x=5", pass kwargs=((:x, 5),). By default, ()
#   tol: Optional numerical tolerance when checking output equality. The default
#     value is 0
#   checks: Optional array of test checks generated by generateCheck(). By
#     default, empty
#
function generateTestCase(args; kwargs=(), tol=_DEFAULT_TOL, checks=_DEFAULT_CHECKS)
    return Dict(
        "args" => _wrapAsTuple(args),
        "kwargs" => kwargs,
        "tol" => tol,
        "checks" => checks,
    )
end

#
# Run an array of test cases
#
# Args:
#   testCases: An array of test cases generated by generateTestCase()
#   graderName: The name of the _grader script that called this function
#
function runTestCases(testCases, graderName)
    try
        # Get functions
        baseName = _getBaseFilename(graderName)
        userFcn = _getEmbeddedFunction(baseName; moduleName="user")
        solFcn = _getEmbeddedFunction(baseName * SOLUTION_POSTFIX; moduleName="sol")

        # Run test cases
        testResults = [_runTestCase(testCase, userFcn, solFcn) for testCase in testCases]

        # Handle results
        success = [testResult["success"] for testResult in testResults]
        testStatus = all(success) ? PASS : FAIL

        # Generate log file
        log = _generateLogFile(testResults)

        # Report test results
        reportTestResults(testStatus; log=log)
    catch
        # Handle error
        reportTestResults(ERROR)
        rethrow()
    end
end

#
# Report test results
#
# Note: Only needs to be called if _grader script doesn't use the
#       generateTestCase() and runTestCases() API, which would  happen, e.g.,
#       if you're grading a script (not a function)
#
# Args:
#   testStatus: PASS, FAIL, or ERROR
#   log: A log string to append to results
#
function reportTestResults(testStatus; log=nothing)
    println(TEST_RESULT_REGEX)
    println(testStatus)
    if !isa(log, Nothing)
        println(log)
    end
end

# Context manager that suppresses STDOUT
#
# Syntax:
#
#   with_NoStdout() do
#       # Commands here
#   end
#
function with_NoStdout(executeBody::Function)
    # Suppress STDOUT
    orig_stdout = stdout
    redirect_stdout()
    try
        # Execute body
        executeBody()
    finally
        # Reconnect STDOUT
        redirect_stdout(orig_stdout)
    end
end

# Implementation of include() that searches on LOAD_PATH for the file
#
# NOTE: file should be a file name (with extension) only, NOT an absolute or
#       relative path
#
function include_from_LOAD_PATH(file)
    Base.include(Main, _getAbsPath(file))
end

#-------------------------------------------------------------------------------
# Private methods
#-------------------------------------------------------------------------------
# Run single test case
function _runTestCase(testCase, userFcn, solFcn)
    userOut, userTime = _callFcn(userFcn, testCase["args"], testCase["kwargs"])
    solOut, solTime = _callFcn(solFcn, testCase["args"], testCase["kwargs"])
    err = [_computeError(user, sol) for (user, sol) in zip(userOut, solOut)]
    _applyTestChecks(userOut, testCase["checks"])
    return _generateTestResult(userTime, solTime, err, testCase["tol"])
end

# Call function
function _callFcn(fcn, args, kwargs)
    with_NoStdout() do
        if VERSION >= v"0.6"
            inner() = fcn(args...; kwargs...)

            # Call once to compile
            Base.invokelatest(inner)

            # Now call for real
            fcnTime = @elapsed fcnOut = _wrapAsTuple(Base.invokelatest(inner))
            return fcnOut, fcnTime
        else
            # Call once to compile
            fcn(args...; kwargs...)

            # Now call for real
            tic()
            fcnOut = _wrapAsTuple(fcn(args...; kwargs...))
            fcnTime = toq()
            return fcnOut, fcnTime
        end
    end
end

# Apply test checks
function _applyTestChecks(out, checks)
    for check in checks
        if !check["fcn"](out[check["arg"]])
            _raiseError(check["msg"]; exception=AssertionError)
        end
    end
end

# Compute error
function _computeError(user, sol)
    _assertCompatibleTypes(user, sol) # Check types
    if isa(sol, String)
        # String
        return float(!(user == sol))
    elseif _isNumericArray(sol)
        # Numeric array
        _assertCompatibleSizeArrays(user, sol) # Check sizes
        return _maxAbsError(user, sol)
    else
        # Unsupported type
        _raiseError(@sprintf("Unsupported output type: %s", typeof(sol)))
    end
end

# Generate test result
function _generateTestResult(userTime, solTime, err, tol)
    return Dict(
        "userTime" => userTime,
        "solTime" => solTime,
        "err" => err,
        "tol" => tol,
        "success" => (maximum(err) <= tol),
    )
end

# Get absolute path to given file
function _getAbsPath(file)
    # Search for file on LOAD_PATH
    for path in LOAD_PATH
        abs_path = joinpath(path, file)
        if isfile(abs_path)
            return abs_path
        end
    end

    # Couldn't find file
    _raiseError(@sprintf("Couldn't find file %s on LOAD_PATH", file); exception=ErrorException)
end

# Get base filename for the input grader file
function _getBaseFilename(graderName)
    filename = splitext(basename(graderName))[1]
    return replace(filename, Regex("$GRADER_POSTFIX\$") => "")
end

