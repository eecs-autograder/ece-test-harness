# Generate log file
function _generateLogFile(testResults)
    # Initialize mesasge
    log = "----- Automated test log -----\n"

    # Append aggregate stats
    nPassed = sum([testResult["success"] for testResult in testResults])
    log *= @sprintf("%d/%d tests passed\n", nPassed, length(testResults))

    # Append test case stats
    for (idx, testResult) in enumerate(testResults)
        errStr = join([@sprintf("%.4g", err) for err in testResult["err"]], ", ")
        log *= @sprintf("\nTest case #%d:\n", idx)
        log *= @sprintf("user time = %.4gs\n", testResult["userTime"])
        log *= @sprintf("sol time  = %.4gs\n", testResult["solTime"])
        log *= @sprintf("error     = %s\n", errStr)
        log *= @sprintf("tolerance = %.4g\n", testResult["tol"])
        log *= @sprintf("status    = %s\n", testResult["success"] ? PASS : FAIL)
    end

    # Return log
    return log
end

# If the input is not a tuple, wrap it as a one-element tuple
function _wrapAsTuple(obj)
    return isa(obj, Nothing) || isa(obj, Tuple) ? obj : (obj,)
end

# Convert to array, if necessary. Doesn't copy array, if possible
function _toArray(array_like)
    return isa(array_like, AbstractArray) ? array_like : collect(array_like)
end

# Determine if the input array_like object is a vector
function _isVector(array_like)
    a = _toArray(array_like)
    return (ndims(a) <= 2) && (count(d -> (d != 1), size(a)) <= 1)
end

# Determine if the input is a numeric array_like object
function _isNumericArray(array_like)
    return eltype(array_like) <: Number
end

# Get number of elements in array_like
function _numel(array_like)
    return length(_toArray(array_like))
end

# Standardize array
function _standardizeArray(array_like)
    a = _toArray(array_like)
    return _isVector(a) ? vec(a) : a
end

# Compute max absolute error
function _maxAbsError(user, sol)
    return maximum(abs, _standardizeArray(user) - _standardizeArray(sol))
end

# Raise informative error if inputs aren't of compatible types
function _assertCompatibleTypes(user, sol)
    # Convenience function for raising the exception
    function _raiseWrongTypeException()
        _raiseError(@sprintf("Received output of %s but expected output of %s", typeof(user), typeof(sol)))
    end

    # Check data types
    if isa(sol, String)
        # String
        if !isa(user, String)
            _raiseWrongTypeException()
        end
    elseif _isNumericArray(sol)
        # Numeric array
        if !_isNumericArray(user)
            _raiseWrongTypeException()
        end
    else
        # Unsupported type
        _raiseError(@sprintf("Unsupported output type: %s", typeof(sol)))
    end
end

# Raise informative error if input array_like objects aren't of compatible sizes
function _assertCompatibleSizeArrays(user, sol)
    # Parse inputs
    user = _toArray(user)
    sol = _toArray(sol)

    # Check compatibility
    if size(_standardizeArray(user)) != size(_standardizeArray(sol))
        _raiseError(@sprintf("Received array of size %s but expected array of size %s", size(user), size(sol)))
    end
end

# Raise error
function _raiseError(msg; exception=ArgumentError)
    pad = "*" ^ 3
    bar = "*" ^ (length(msg) + 2)
    throw(exception("\n\n$pad$bar$pad\n$pad $msg $pad\n$pad$bar$pad\n"))
end

